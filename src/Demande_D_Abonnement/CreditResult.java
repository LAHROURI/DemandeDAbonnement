  public final void ONEP(
                  double Lang_Br,
                  int NR_Etage,
                  float Sup_LOT,
                  float Sup_Second,
                  float Avance_Even,
                  int NBTrim
                        ){
    double Prix_Branch,Re = 6.42;
    int n , TVA=20 ,FD=20;
      double Proviss=1168.42 ;
      double Taxe_Riv;
////// START Branchemen Eau
////// INPUT Lang_Br (Longeur de Branchement)
if(Lang_Br<=10) Prix_Branch=1936.40 ;
else if(Lang_Br<=20) Prix_Branch=2572.92 ;
     else if(Lang_Br<=40) Prix_Branch=3604.22 ;
	   else { Prix_Branch=(Lang_Br*65.68) + 1525.85 ;}
////// END Branchemen Eau
////// OUTPUT Prix_Branch (Prix de Branchement)

////// START Taxe_Riv (Taxe Riveraire)
////// Taxe_Riv= Po * ( R + n * r )^(1/2)
////// INPUT NR_Etage(nombre des etages) ,Sup_LOT, r
////// Const Po
if (NR_Etage<3)n=0;
else if(NR_Etage==3) n=1;
	 else n=2;
double Po = (507.19 * (100+TVA))/100;
     Taxe_Riv = Po * (Math.sqrt(Sup_LOT + n*Sup_Second));
///// END Taxe_Riv (Taxe Riveraire)
///// OUTPUT Taxe_Riv (taxe riveraire)

///// START PAYMENT
///// INPUT Prix_Branch , Taxe_Riv 
///// CONST Proviss , Re = 6.42 ,FD(frais documentaire)=20

///START CREDIT
      double Coefficients[]={1.000,0.509,0.342,0.258,
                             0.208,0.174,0.150,0.132,
                             0.118,0.107,0.098,0.090,
                             0.084,0.078,0.074,0.069,
                             0.066,0.062,0.059,0.057,
                             0.054,0.052,0.050,0.048,
                             0.047,0.045,0.044,0.043};
      double CrA = Prix_Branch + Taxe_Riv - Avance_Even ; 
      double C =Coefficients[NBTrim];
      double Trim=CrA*C;
      double Credit_BS= Prix_Branch+Taxe_Riv;
      double TimbreDrs =((Prix_Branch+Taxe_Riv+Proviss+Re)*0.0025)+ FD;
      double total=TimbreDrs+Trim+6.42+ onp.getprovission();

      lbbr(     String.valueOf(String.format("%.3f",Credit_BS)),
                String.valueOf(String.format("%.3f",Prix_Branch)),
                String.valueOf(String.format("%.3f",Taxe_Riv)),
                String.valueOf(String.format("%.3f",Trim)),
                String.valueOf(String.format("%.3f",TimbreDrs)),
                String.valueOf(String.format("%.3f",total)),
                String.valueOf(String.format("%.3f",onp.getprovission())));
///END CREDIT
     
///// END PAYMENT

      };
